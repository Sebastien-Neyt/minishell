#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (needle[i] == '\0')
		return ((char *)haystack);
	while (haystack[i] && i < len)
	{
		j = 0;
		if (haystack[i] == needle[j])
		{
			while (haystack[i] == needle[j] && i < len && haystack[i])
			{
				i++;
				j++;
			}
			if (needle[j] == '\0')
				return ((char *)(haystack + i - j));
			i -= j;
		}
		i++;
	}
	return (NULL);
}
